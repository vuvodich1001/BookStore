    private void setTabledata(List<Customer> list){
        defaultTableModel.setRowCount(0);
        for(Customer m : list){
            defaultTableModel.addRow(new Object[]{m.getFullName(), m.getEmail(), m.getAddress(),
            m.getCity(), m.getCountry(), m.getZipcode(), m.getPassword()});
        }
    }
