    private void ItemDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ItemDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblCustomer.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "You need to select row first!");
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(this, "are you sure to delete this row!", "Message" , JOptionPane.YES_NO_OPTION);
            if(confirm == JOptionPane.YES_OPTION){
                String full_name = String.valueOf(tblCustomer.getValueAt(row, 0));
                for(Customer c : customerService.getAllcustomer()){
                    if(full_name.equals(c.getFullName())){
                        customerService.deleteCustomer(c);
                        setTabledata(customerService.getAllcustomer());
                    }
                }
            }
        }
    }//GEN-LAST:event_ItemDeleteActionPerformed

